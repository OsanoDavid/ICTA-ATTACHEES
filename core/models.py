# 4. Attachée Profiles (Strictly maps 1 attachée to exactly 1 department)
class AttacheeProfile(models.Model):
    GENDER_CHOICES = (
        ('M', 'Male'),
        ('F', 'Female'),
    )
    
    STATUS_CHOICES = (
        ('PENDING', 'Pending Approval'),
        ('APPROVED', 'Approved (Waiting to Start)'),
        ('ACTIVE', 'Active on Program'),
        ('COMPLETED', 'Attachment Finished'),
        ('REJECTED', 'Rejected'),
    )

    user = models.OneToOneField(User, on_delete=models.CASCADE, related_name='attachee_profile')
    full_name = models.CharField(max_length=100)
    gender = models.CharField(max_length=1, choices=GENDER_CHOICES)
    national_id = models.CharField(max_length=20, unique=True, null=True, blank=True)
    registration_number = models.CharField(max_length=50, unique=True)
    course_name = models.CharField(max_length=150) # e.g., BSc. Computer Science
    institution = models.CharField(max_length=150) # e.g., Kisii University
    
    # ForeignKey ensures an attachée can ONLY belong to one department
    department = models.ForeignKey(Department, on_delete=models.PROTECT, related_name='attachees')
    status = models.CharField(max_length=20, choices=STATUS_CHOICES, default='PENDING')
    # Attachment period dates
    attachment_start_date = models.DateField(null=True, blank=True, help_text="Start date of industrial attachment")
    attachment_end_date = models.DateField(null=True, blank=True, help_text="End date of industrial attachment")
    created_at = models.DateTimeField(auto_now_add=True)

    def update_status(self):
        """
        Automatically transitions status based on current date.
        PENDING/REJECTED stays as is unless manually changed.
        APPROVED -> ACTIVE when today >= start_date
        ACTIVE/APPROVED -> COMPLETED when today > end_date
        """
        from django.utils import timezone
        today = timezone.now().date()
        
        changed = False
        
        # Rule 1: Auto-complete if end date passed (highest priority)
        if self.status in ['APPROVED', 'ACTIVE'] and self.attachment_end_date:
            if today > self.attachment_end_date:
                self.status = 'COMPLETED'
                changed = True
        
        # Rule 2: Activate if start date reached and not yet completed
        if self.status == 'APPROVED' and self.attachment_start_date:
            if today >= self.attachment_start_date and today <= (self.attachment_end_date or today):
                self.status = 'ACTIVE'
                changed = True
                
        return changed

    def save(self, *args, **kwargs):
        # Trigger auto-status update before saving
        self.update_status()
        super().save(*args, **kwargs)

    def __str__(self):
        return f"{self.full_name} ({self.registration_number})"


