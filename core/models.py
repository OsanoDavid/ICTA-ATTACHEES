class RosterConfiguration(models.Model):
    ROSTER_MODES = (
        ('CONSTANT', 'Constant Fixed Days (Select Days Below)'),
        ('INTERVAL_1', '1 Day Gap (On-Site, 1 Day Remote, On-Site...)'),
        ('INTERVAL_2', '2 Days Gap (On-Site, 2 Days Remote, On-Site...)'),
        ('INTERVAL_3', '3 Days Gap (On-Site, 3 Days Remote, On-Site...)'),
        ('INTERVAL_4', '4 Days Gap (On-Site, 4 Days Remote, On-Site...)'),
    )

    # Changed to ManyToManyField to allow bulk assignment
    attachees = models.ManyToManyField(
        'AttacheeProfile', 
        related_name='roster_configs'
    )
    generation_mode = models.CharField(
        max_length=20, 
        choices=ROSTER_MODES, 
        default='CONSTANT',
        verbose_name="Scheduling Pattern Rule"
    )
    
    # Used ONLY if CONSTANT mode is selected
    constant_monday = models.BooleanField(default=False, verbose_name="Constant Monday")
    constant_tuesday = models.BooleanField(default=False, verbose_name="Constant Tuesday")
    constant_wednesday = models.BooleanField(default=False, verbose_name="Constant Wednesday")
    constant_thursday = models.BooleanField(default=False, verbose_name="Constant Thursday")
    constant_friday = models.BooleanField(default=False, verbose_name="Constant Friday")
    
    # Used to anchor the rolling intervals calculation
    rotation_start_date = models.DateField(
        default=datetime.date.today, 
        verbose_name="Rotation Anchor Start Date",
        help_text="The first day the rolling sequence logic begins calculations."
    )

    class Meta:
        verbose_name = "Roster Configuration"
        verbose_name_plural = "Roster Configurations"

    def __str__(self):
        return f"Roster Rule ({self.get_generation_mode_display()}) applied to {self.attachees.count()} attachees"


class GeneratedShift(models.Model):
    attachee = models.ForeignKey('AttacheeProfile', on_delete=models.CASCADE, related_name='generated_shifts')
    date = models.DateField()
    day_name = models.CharField(max_length=15)
    work_mode = models.CharField(max_length=15, choices=(('PHYSICAL', 'On-Site'), ('REMOTE', 'Remote')))

    class Meta:
        unique_together = ('attachee', 'date')
        ordering = ['date']

    def __str__(self):
        return f"{self.attachee.full_name} - {self.date} ({self.work_mode})"
    
from django.db.models.signals import post_save
from django.dispatch import receiver

@receiver(post_save, sender=RosterConfiguration)
def execute_algorithmic_schedule(sender, instance, **kwargs):
    """
    Computes precise calendar dates for multiple attachees based on a shared rule.
    """
    # This signal now runs for each configuration. We iterate through the associated attachees.
    # Note: The m2m_changed signal is often better for ManyToMany, but post_save works for creation.
    for attachee in instance.attachees.all():
        # Wipe out future assignments to recalculate cleanly from the start date
        GeneratedShift.objects.filter(attachee=attachee, date__gte=instance.rotation_start_date).delete()
        
        start_date = instance.rotation_start_date
        shifts_to_create = []
        
        # Determine the remote gap size if an interval mode is chosen
        interval_gaps = {
            'INTERVAL_1': 1,
            'INTERVAL_2': 2,
            'INTERVAL_3': 3,
            'INTERVAL_4': 4
        }
        
        gap_size = interval_gaps.get(instance.generation_mode, 0)
        workday_counter = 0  # Tracks working days sequentially (ignoring Sat/Sun)

        # Pre-calculate a rolling 60-day path
        for i in range(60):
            current_date = start_date + datetime.timedelta(days=i)
            day_str = current_date.strftime('%A')
            
            # STRICT RULE: Skip weekends entirely from logic
            if day_str in ['Saturday', 'Sunday']:
                continue
                
            mode = 'REMOTE'
            
            # LOGIC 1: CONSTANT LOCKED DAYS
            if instance.generation_mode == 'CONSTANT':
                if day_str == 'Monday' and instance.constant_monday: mode = 'PHYSICAL'
                elif day_str == 'Tuesday' and instance.constant_tuesday: mode = 'PHYSICAL'
                elif day_str == 'Wednesday' and instance.constant_wednesday: mode = 'PHYSICAL'
                elif day_str == 'Thursday' and instance.constant_thursday: mode = 'PHYSICAL'
                elif day_str == 'Friday' and instance.constant_friday: mode = 'PHYSICAL'
                
            # LOGIC 2: ROLLING WORKDAY INTERVAL GAPS
            else:
                # On-Site hits whenever the workday counter hits 0 after completing a gap cycle
                if workday_counter % (gap_size + 1) == 0:
                    mode = 'PHYSICAL'
                workday_counter += 1

            shifts_to_create.append(
                GeneratedShift(
                    attachee=attachee,
                    date=current_date,
                    day_name=day_str,
                    work_mode=mode
                )
            )
            
        GeneratedShift.objects.bulk_create(shifts_to_create)
